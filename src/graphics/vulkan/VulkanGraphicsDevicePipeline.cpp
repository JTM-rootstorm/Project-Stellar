std::expected<void, stellar::platform::Error> VulkanGraphicsDevice::create_pipeline_layout() {
    const VkPushConstantRange push_constant_range{
        .stageFlags = VK_SHADER_STAGE_VERTEX_BIT | VK_SHADER_STAGE_FRAGMENT_BIT,
        .offset = 0,
        .size = sizeof(VulkanDrawPushConstants),
    };
    const VkDescriptorSetLayout set_layouts[] = {material_descriptor_set_layout_};
    const VkPipelineLayoutCreateInfo create_info{
        .sType = VK_STRUCTURE_TYPE_PIPELINE_LAYOUT_CREATE_INFO,
        .setLayoutCount = 1,
        .pSetLayouts = set_layouts,
        .pushConstantRangeCount = 1,
        .pPushConstantRanges = &push_constant_range,
    };

    const VkResult result = vkCreatePipelineLayout(device_, &create_info, nullptr,
                                                   &pipeline_layout_);
    if (result != VK_SUCCESS) {
        return std::unexpected(vulkan_error("vkCreatePipelineLayout", result));
    }
    return {};
}

std::expected<void, stellar::platform::Error> VulkanGraphicsDevice::create_graphics_pipeline() {
    auto vertex_shader = create_shader_module(device_, kMaterialVertexShaderSpv,
                                              std::size(kMaterialVertexShaderSpv));
    if (!vertex_shader) {
        return std::unexpected(vertex_shader.error());
    }
    auto fragment_shader = create_shader_module(device_, kMaterialFragmentShaderSpv,
                                                std::size(kMaterialFragmentShaderSpv));
    if (!fragment_shader) {
        vkDestroyShaderModule(device_, *vertex_shader, nullptr);
        return std::unexpected(fragment_shader.error());
    }

    const VkPipelineShaderStageCreateInfo shader_stages[] = {
        VkPipelineShaderStageCreateInfo{
            .sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO,
            .stage = VK_SHADER_STAGE_VERTEX_BIT,
            .module = *vertex_shader,
            .pName = "main",
        },
        VkPipelineShaderStageCreateInfo{
            .sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO,
            .stage = VK_SHADER_STAGE_FRAGMENT_BIT,
            .module = *fragment_shader,
            .pName = "main",
        },
    };
    const VkVertexInputBindingDescription binding_description{
        .binding = 0,
        .stride = sizeof(stellar::assets::StaticVertex),
        .inputRate = VK_VERTEX_INPUT_RATE_VERTEX,
    };
    const VkVertexInputAttributeDescription attribute_descriptions[] = {
        VkVertexInputAttributeDescription{
            .location = 0,
            .binding = 0,
            .format = VK_FORMAT_R32G32B32_SFLOAT,
            .offset = offsetof(stellar::assets::StaticVertex, position),
        },
        VkVertexInputAttributeDescription{
            .location = 1,
            .binding = 0,
            .format = VK_FORMAT_R32G32B32_SFLOAT,
            .offset = offsetof(stellar::assets::StaticVertex, normal),
        },
        VkVertexInputAttributeDescription{
            .location = 2,
            .binding = 0,
            .format = VK_FORMAT_R32G32_SFLOAT,
            .offset = offsetof(stellar::assets::StaticVertex, uv0),
        },
        VkVertexInputAttributeDescription{
            .location = 3,
            .binding = 0,
            .format = VK_FORMAT_R32G32B32A32_SFLOAT,
            .offset = offsetof(stellar::assets::StaticVertex, tangent),
        },
        VkVertexInputAttributeDescription{
            .location = 4,
            .binding = 0,
            .format = VK_FORMAT_R32G32_SFLOAT,
            .offset = offsetof(stellar::assets::StaticVertex, uv1),
        },
        VkVertexInputAttributeDescription{
            .location = 5,
            .binding = 0,
            .format = VK_FORMAT_R32G32B32A32_SFLOAT,
            .offset = offsetof(stellar::assets::StaticVertex, color),
        },
    };
    const VkPipelineVertexInputStateCreateInfo vertex_input_info{
        .sType = VK_STRUCTURE_TYPE_PIPELINE_VERTEX_INPUT_STATE_CREATE_INFO,
        .vertexBindingDescriptionCount = 1,
        .pVertexBindingDescriptions = &binding_description,
        .vertexAttributeDescriptionCount = std::size(attribute_descriptions),
        .pVertexAttributeDescriptions = attribute_descriptions,
    };
    const VkPipelineInputAssemblyStateCreateInfo input_assembly{
        .sType = VK_STRUCTURE_TYPE_PIPELINE_INPUT_ASSEMBLY_STATE_CREATE_INFO,
        .topology = VK_PRIMITIVE_TOPOLOGY_TRIANGLE_LIST,
    };
    const VkViewport viewport{
        .x = 0.0F,
        .y = 0.0F,
        .width = static_cast<float>(swapchain_extent_.width),
        .height = static_cast<float>(swapchain_extent_.height),
        .minDepth = 0.0F,
        .maxDepth = 1.0F,
    };
    const VkRect2D scissor{{0, 0}, swapchain_extent_};
    const VkPipelineViewportStateCreateInfo viewport_state{
        .sType = VK_STRUCTURE_TYPE_PIPELINE_VIEWPORT_STATE_CREATE_INFO,
        .viewportCount = 1,
        .pViewports = &viewport,
        .scissorCount = 1,
        .pScissors = &scissor,
    };
    VkPipelineRasterizationStateCreateInfo rasterizer{
        .sType = VK_STRUCTURE_TYPE_PIPELINE_RASTERIZATION_STATE_CREATE_INFO,
        .polygonMode = VK_POLYGON_MODE_FILL,
        .cullMode = VK_CULL_MODE_BACK_BIT,
        .frontFace = VK_FRONT_FACE_CLOCKWISE,
        .lineWidth = 1.0F,
    };
    const VkPipelineMultisampleStateCreateInfo multisampling{
        .sType = VK_STRUCTURE_TYPE_PIPELINE_MULTISAMPLE_STATE_CREATE_INFO,
        .rasterizationSamples = VK_SAMPLE_COUNT_1_BIT,
    };
    const VkPipelineDepthStencilStateCreateInfo depth_stencil{
        .sType = VK_STRUCTURE_TYPE_PIPELINE_DEPTH_STENCIL_STATE_CREATE_INFO,
        .depthTestEnable = VK_TRUE,
        .depthWriteEnable = VK_TRUE,
        .depthCompareOp = VK_COMPARE_OP_LESS,
    };
    VkPipelineColorBlendAttachmentState color_blend_attachment{
        .colorWriteMask = VK_COLOR_COMPONENT_R_BIT | VK_COLOR_COMPONENT_G_BIT |
            VK_COLOR_COMPONENT_B_BIT | VK_COLOR_COMPONENT_A_BIT,
    };
    const VkPipelineColorBlendStateCreateInfo color_blending{
        .sType = VK_STRUCTURE_TYPE_PIPELINE_COLOR_BLEND_STATE_CREATE_INFO,
        .attachmentCount = 1,
        .pAttachments = &color_blend_attachment,
    };
    const VkGraphicsPipelineCreateInfo pipeline_info{
        .sType = VK_STRUCTURE_TYPE_GRAPHICS_PIPELINE_CREATE_INFO,
        .stageCount = std::size(shader_stages),
        .pStages = shader_stages,
        .pVertexInputState = &vertex_input_info,
        .pInputAssemblyState = &input_assembly,
        .pViewportState = &viewport_state,
        .pRasterizationState = &rasterizer,
        .pMultisampleState = &multisampling,
        .pDepthStencilState = &depth_stencil,
        .pColorBlendState = &color_blending,
        .layout = pipeline_layout_,
        .renderPass = render_pass_,
        .subpass = 0,
    };

    struct PipelineVariant {
        VkCullModeFlags cull_mode;
        bool blend_enabled;
        VkPipeline* pipeline;
    };
    const PipelineVariant variants[] = {
        PipelineVariant{VK_CULL_MODE_BACK_BIT, false, &graphics_pipeline_},
        PipelineVariant{VK_CULL_MODE_NONE, false, &graphics_pipeline_double_sided_},
        PipelineVariant{VK_CULL_MODE_BACK_BIT, true, &alpha_blend_pipeline_},
        PipelineVariant{VK_CULL_MODE_NONE, true, &alpha_blend_pipeline_double_sided_},
    };

    VkResult result = VK_SUCCESS;
    for (const PipelineVariant& variant : variants) {
        rasterizer.cullMode = variant.cull_mode;
        color_blend_attachment.blendEnable = variant.blend_enabled ? VK_TRUE : VK_FALSE;
        color_blend_attachment.srcColorBlendFactor = VK_BLEND_FACTOR_SRC_ALPHA;
        color_blend_attachment.dstColorBlendFactor = VK_BLEND_FACTOR_ONE_MINUS_SRC_ALPHA;
        color_blend_attachment.colorBlendOp = VK_BLEND_OP_ADD;
        color_blend_attachment.srcAlphaBlendFactor = VK_BLEND_FACTOR_SRC_ALPHA;
        color_blend_attachment.dstAlphaBlendFactor = VK_BLEND_FACTOR_ONE_MINUS_SRC_ALPHA;
        color_blend_attachment.alphaBlendOp = VK_BLEND_OP_ADD;

        result = vkCreateGraphicsPipelines(device_, VK_NULL_HANDLE, 1, &pipeline_info, nullptr,
                                           variant.pipeline);
        if (result != VK_SUCCESS) {
            break;
        }
    }
    vkDestroyShaderModule(device_, *fragment_shader, nullptr);
    vkDestroyShaderModule(device_, *vertex_shader, nullptr);
    if (result != VK_SUCCESS) {
        if (graphics_pipeline_ != VK_NULL_HANDLE) {
            vkDestroyPipeline(device_, graphics_pipeline_, nullptr);
            graphics_pipeline_ = VK_NULL_HANDLE;
        }
        if (graphics_pipeline_double_sided_ != VK_NULL_HANDLE) {
            vkDestroyPipeline(device_, graphics_pipeline_double_sided_, nullptr);
            graphics_pipeline_double_sided_ = VK_NULL_HANDLE;
        }
        if (alpha_blend_pipeline_ != VK_NULL_HANDLE) {
            vkDestroyPipeline(device_, alpha_blend_pipeline_, nullptr);
            alpha_blend_pipeline_ = VK_NULL_HANDLE;
        }
        if (alpha_blend_pipeline_double_sided_ != VK_NULL_HANDLE) {
            vkDestroyPipeline(device_, alpha_blend_pipeline_double_sided_, nullptr);
            alpha_blend_pipeline_double_sided_ = VK_NULL_HANDLE;
        }
        return std::unexpected(vulkan_error("vkCreateGraphicsPipelines", result));
    }
    return {};
}
